                    <p>What goes into a decision? More than meets the eye. There are a lot of things that can influence our choices: some are negative, and some are positive. The career we choose might be influenced by our family’s expectations, and the clothes we buy might be influenced by the ads we see.</p>
                    <p>There are many social forces playing out in our lives — at home, work, school, even when we’re hanging out. Some of these influences help us to progress. For instance, a good coach can teach us the discipline we need to excel at sports. On the other hand, some influences hold us back. For instance, trying to achieve someone else’s narrow standard of physical beauty can be demoralizing, not to mention a distraction from our desire to change the world.</p>
                    <p>We often see the negative influences. They can be obvious, like conflict, gossip, violence, and economic inequality. Yet, we are challenged to see root causes as we try to address them. At the same time, we often overlook the positive forces that surround us, like the spirit of volunteerism and the firm hope that things can advance through our efforts.</p>
                    <p>Being able to recognize the influences in our lives helps us to make choices without falling prey to destructive forces. Perhaps, then, instead of contributing to systems that are causing harm, we can transform them for the better.</p>
